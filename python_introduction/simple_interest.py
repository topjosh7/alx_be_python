# simple_interest.py

# Define the principal amount, interest rate, and time (in years)
principal = 1000  # principal amount in currency units
rate = 0.05       # annual interest rate (5%)
time = 3          # time in years

# Calculate the simple interest using the formula I = P * R * T
interest = principal * rate * time

# Print the result in a human-readable format
print("The simple interest for a principal of", principal, "at an annual rate of", rate, "over", time, "years is", interest)
