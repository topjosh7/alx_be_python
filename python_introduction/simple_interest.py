# simple_interest.py

# Define the principal amount, interest rate, and time (in years)
P = 1000  # principal amount in currency units
R = 0.05  # annual interest rate (5%)
T = 3     # time in years

# Calculate the simple interest
I = P * R * T

# Print the result in a human-readable format
print("The simple interest for a principal of", P, "at an annual rate of", R, "over", T, "years is", I)

